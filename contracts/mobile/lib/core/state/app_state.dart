/// State for the interactive knowledge graph visualization.
class KnowledgeGraphState {
  const KnowledgeGraphState({
    this.graph,
    this.selectedNodeId,
    this.subjectFilter,
    this.zoomLevel = 1.0,
    this.panOffsetX = 0.0,
    this.panOffsetY = 0.0,
    this.isLoading = false,
    this.error,
    this.searchQuery,
    this.highlightedPath = const [],
  });

  final KnowledgeGraph? graph;
  final String? selectedNodeId;
  final Subject? subjectFilter;
  final double zoomLevel;
  final double panOffsetX;
  final double panOffsetY;
  final bool isLoading;
  final String? error;
  final String? searchQuery;

  /// Node IDs along the highlighted prerequisite path.
  final List<String> highlightedPath;

  ConceptNode? get selectedNode => graph?.nodes
      .cast<ConceptNode?>()
      .firstWhere((n) => n?.conceptId == selectedNodeId, orElse: () => null);

  List<ConceptNode> get filteredNodes {
    if (graph == null) return [];
    if (subjectFilter == null) return graph!.nodes;
    return graph!.nodes.where((n) => n.subject == subjectFilter).toList();
  }

  KnowledgeGraphState copyWith({
    KnowledgeGraph? graph,
    String? selectedNodeId,
    Subject? subjectFilter,
    double? zoomLevel,
    double? panOffsetX,
    double? panOffsetY,
    bool? isLoading,
    String? error,
    String? searchQuery,
    List<String>? highlightedPath,
  }) {
    return KnowledgeGraphState(
      graph: graph ?? this.graph,
      selectedNodeId: selectedNodeId ?? this.selectedNodeId,
      subjectFilter: subjectFilter ?? this.subjectFilter,
      zoomLevel: zoomLevel ?? this.zoomLevel,
      panOffsetX: panOffsetX ?? this.panOffsetX,
      panOffsetY: panOffsetY ?? this.panOffsetY,
      isLoading: isLoading ?? this.isLoading,
      error: error ?? this.error,
      searchQuery: searchQuery ?? this.searchQuery,
      highlightedPath: highlightedPath ?? this.highlightedPath,
    );
  }
}

/// Manages the knowledge graph visualization state.
///
/// Receives graph updates from the WebSocket service and handles
/// user interactions (select node, filter by subject, zoom, pan).
class KnowledgeGraphNotifier extends StateNotifier<KnowledgeGraphState> {
  KnowledgeGraphNotifier({
    required this.webSocketService,
  }) : super(const KnowledgeGraphState()) {
    _subscribeToEvents();
  }

  final WebSocketService webSocketService;
  StreamSubscription<MessageEnvelope>? _subscription;

  /// Select a concept node in the graph.
  void selectNode(String? conceptId) {
    state = state.copyWith(selectedNodeId: conceptId);
  }

  /// Filter the graph to show only one subject.
  void filterBySubject(Subject? subject) {
    state = state.copyWith(subjectFilter: subject);
  }

  /// Update zoom level (clamped to [0.3, 3.0]).
  void setZoom(double zoom) {
    state = state.copyWith(zoomLevel: zoom.clamp(0.3, 3.0));
  }

  /// Update pan offset.
  void setPanOffset(double x, double y) {
    state = state.copyWith(panOffsetX: x, panOffsetY: y);
  }

  /// Search concepts by name.
  void search(String? query) {
    state = state.copyWith(searchQuery: query);
  }

  /// Highlight the prerequisite path to a target concept.
  void highlightPathTo(String conceptId) {
    // Implementation: BFS/DFS from root concepts to the target,
    // collecting the path of concept IDs.
    state = state.copyWith(highlightedPath: [conceptId]);
  }

  /// Clear the highlighted path.
  void clearHighlight() {
    state = state.copyWith(highlightedPath: []);
  }

  void _subscribeToEvents() {
    // Implementation: listen to KnowledgeGraphUpdated events from WebSocket
  }

  @override
  void dispose() {
    _subscription?.cancel();
    super.dispose();
  }
}

