/// State for the active learning session.
class SessionState {
  const SessionState({
    this.currentSession,
    this.currentExercise,
    this.methodology,
    this.fatigueScore = 0.0,
    this.questionsAttempted = 0,
    this.questionsCorrect = 0,
    this.isLoading = false,
    this.error,
    this.isBreakSuggested = false,
    this.hintsUsed = 0,
    this.sessionHistory = const [],
  });

  final Session? currentSession;
  final Exercise? currentExercise;
  final Methodology? methodology;
  final double fatigueScore;
  final int questionsAttempted;
  final int questionsCorrect;
  final bool isLoading;
  final String? error;
  final bool isBreakSuggested;
  final int hintsUsed;

  /// Recent answer results for the progress display.
  final List<AnswerResult> sessionHistory;

  bool get isActive => currentSession != null && currentSession!.endedAt == null;
  double get accuracy =>
      questionsAttempted > 0 ? questionsCorrect / questionsAttempted : 0.0;
  Duration get elapsed => currentSession != null
      ? DateTime.now().difference(currentSession!.startedAt)
      : Duration.zero;

  SessionState copyWith({
    Session? currentSession,
    Exercise? currentExercise,
    Methodology? methodology,
    double? fatigueScore,
    int? questionsAttempted,
    int? questionsCorrect,
    bool? isLoading,
    String? error,
    bool? isBreakSuggested,
    int? hintsUsed,
    List<AnswerResult>? sessionHistory,
  }) {
    return SessionState(
      currentSession: currentSession ?? this.currentSession,
      currentExercise: currentExercise ?? this.currentExercise,
      methodology: methodology ?? this.methodology,
      fatigueScore: fatigueScore ?? this.fatigueScore,
      questionsAttempted: questionsAttempted ?? this.questionsAttempted,
      questionsCorrect: questionsCorrect ?? this.questionsCorrect,
      isLoading: isLoading ?? this.isLoading,
      error: error ?? this.error,
      isBreakSuggested: isBreakSuggested ?? this.isBreakSuggested,
      hintsUsed: hintsUsed ?? this.hintsUsed,
      sessionHistory: sessionHistory ?? this.sessionHistory,
    );
  }
}

/// Manages the active learning session lifecycle.
///
/// Listens to WebSocket events for question delivery, answer evaluation,
/// methodology switches, and cognitive load warnings.
class SessionNotifier extends StateNotifier<SessionState> {
  SessionNotifier({
    required this.webSocketService,
    required this.syncManager,
  }) : super(const SessionState()) {
    _subscribeToEvents();
  }

  final WebSocketService webSocketService;
  final SyncManager syncManager;
  final List<StreamSubscription<dynamic>> _subscriptions = [];

  /// Start a new learning session.
  Future<void> startSession({Subject? subject, int durationMinutes = 25}) async {
    state = state.copyWith(isLoading: true, error: null);
    try {
      await webSocketService.startSession(StartSession(
        studentId: '', // Injected from UserNotifier
        subject: subject,
        durationMinutes: durationMinutes,
      ));
    } catch (e) {
      state = state.copyWith(isLoading: false, error: e.toString());
    }
  }

  /// Submit an answer to the current exercise.
  Future<void> submitAnswer(String answer, int timeSpentMs) async {
    final exercise = state.currentExercise;
    if (exercise == null) return;

    await webSocketService.attemptConcept(AttemptConcept(
      sessionId: state.currentSession!.id,
      exerciseId: exercise.id,
      conceptId: exercise.conceptId,
      answer: answer,
      timeSpentMs: timeSpentMs,
      idempotencyKey: '', // Generated by implementation
    ));
  }

  /// Request a hint for the current exercise.
  Future<void> requestHint() async {
    final exercise = state.currentExercise;
    final session = state.currentSession;
    if (exercise == null || session == null) return;

    await webSocketService.requestHint(RequestHint(
      sessionId: session.id,
      exerciseId: exercise.id,
      hintLevel: state.hintsUsed,
    ));
    state = state.copyWith(hintsUsed: state.hintsUsed + 1);
  }

  /// Skip the current question.
  Future<void> skipQuestion({String? reason}) async {
    final exercise = state.currentExercise;
    final session = state.currentSession;
    if (exercise == null || session == null) return;

    await webSocketService.skipQuestion(SkipQuestion(
      sessionId: session.id,
      exerciseId: exercise.id,
      reason: reason,
    ));
  }

  /// Request a different pedagogical approach.
  Future<void> switchApproach(String preferenceHint) async {
    final session = state.currentSession;
    if (session == null) return;

    await webSocketService.switchApproach(SwitchApproach(
      sessionId: session.id,
      preferenceHint: preferenceHint,
    ));
  }

  /// End the current session.
  Future<void> endSession({String reason = 'manual'}) async {
    final session = state.currentSession;
    if (session == null) return;

    await webSocketService.endSession(EndSession(
      sessionId: session.id,
      reason: reason,
    ));
  }

  /// Acknowledge the break suggestion and continue or take a break.
  void dismissBreakSuggestion() {
    state = state.copyWith(isBreakSuggested: false);
  }

  void _subscribeToEvents() {
    // Implementation subscribes to WebSocket message stream and routes events:
    // - QuestionPresented → update currentExercise, increment attempted
    // - AnswerEvaluated → update sessionHistory, increment correct if applicable
    // - MethodologySwitched → update methodology
    // - CognitiveLoadWarning → update fatigueScore, set isBreakSuggested
    // - SessionSummary → mark session ended
  }

  @override
  void dispose() {
    for (final sub in _subscriptions) {
      sub.cancel();
    }
    super.dispose();
  }
}

