// =============================================================================
// Cena Platform — Question Domain Events
// Event-sourced question lifecycle: creation, editing, quality gate, publishing
// Three creation paths: authored, ingested, AI-generated
// =============================================================================

namespace Cena.Actors.Events;

// ── Shared option data (used by creation + language version events) ──

public sealed record QuestionOptionData(
    string Label,
    string Text,
    string TextHtml,
    bool IsCorrect,
    string? DistractorRationale);

// ── Creation Events (3 paths) ──

/// <summary>Manually authored by a human (teacher, content creator).</summary>
public sealed record QuestionAuthored_V1(
    string QuestionId,
    string Stem,
    string StemHtml,
    IReadOnlyList<QuestionOptionData> Options,
    string Subject,
    string Topic,
    string Grade,
    int BloomsLevel,
    float Difficulty,
    IReadOnlyList<string> ConceptIds,
    string Language,
    string AuthorId,
    DateTimeOffset Timestamp,
    string? Explanation = null);

/// <summary>
/// V2 adds <see cref="LearningObjectiveId"/> per FIND-pedagogy-008.
/// Anderson &amp; Krathwohl (2001) + Wiggins &amp; McTighe (2005) require every
/// assessment item to trace to an explicit learning goal. Upcasted from V1
/// with a <c>null</c> LO id so existing streams replay cleanly.
/// </summary>
public sealed record QuestionAuthored_V2(
    string QuestionId,
    string Stem,
    string StemHtml,
    IReadOnlyList<QuestionOptionData> Options,
    string Subject,
    string Topic,
    string Grade,
    int BloomsLevel,
    float Difficulty,
    IReadOnlyList<string> ConceptIds,
    string Language,
    string AuthorId,
    DateTimeOffset Timestamp,
    string? Explanation,
    /// <summary>
    /// Learning-objective id this question assesses. Null means "unknown"
    /// (V1 event) and the projection logs a warning so curriculum authors
    /// can backfill.
    /// </summary>
    string? LearningObjectiveId);

