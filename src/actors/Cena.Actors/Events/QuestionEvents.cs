/// <summary>
/// Emitted when a learning-objective id is assigned to (or changed on)
/// an existing question after initial authoring — e.g., a curriculum author
/// backfills metadata on a V1 question that was created before
/// FIND-pedagogy-008 added the field. Separate event so the change is a
/// first-class entry in the audit history.
/// </summary>
public sealed record LearningObjectiveAssigned_V1(
    string QuestionId,
    string? PreviousObjectiveId,
    string NewObjectiveId,
    string AssignedBy,
    DateTimeOffset Timestamp);

